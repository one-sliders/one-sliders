function replacePhrases(title, dictionary = {}) {
  let out = title;
  for (const [from, to] of Object.entries(dictionary).sort((a, b) => b[0].length - a[0].length)) {
    const escaped = from.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    out = out.replace(new RegExp(`\\b${escaped}\\b`, 'g'), to);
  }
  return out;
}

export function translateEventTitle(title, lang) {
  if (eventTitleTranslations[lang]?.[title]) return eventTitleTranslations[lang][title];
  let out = replacePhrases(title, titlePhraseTranslations[lang]);
  out = replacePhrases(out, placeTerms[lang]);
  return out;
}
