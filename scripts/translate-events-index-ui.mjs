function replaceText(html, from, to) {
  return html.split(from).join(to);
}

function translate(html, t) {
  let out = html;
  out = out.replace(/<title>[^<]*<\/title>/, `<title>${t.title}</title>`);
  out = out.replace(/<span class="hero-kicker">[^<]*<\/span>/, `<span class="hero-kicker">${t.kicker}</span>`);
  out = out.replace(/<h1>World events worth planning around<\/h1>/, `<h1>${t.h1}</h1>`);
  out = out.replace(/<p>Sports, festivals, culture and natural spectacles, arranged month by month\.<\/p>/, `<p>${t.intro}</p>`);
  out = out.replace(/<span>Filters <span class="filter-badge"/, `<span>${t.filters} <span class="filter-badge"`);
  out = replaceText(out, '<span class="filter-label">Category</span>', `<span class="filter-label">${t.category}</span>`);
  out = replaceText(out, '<span class="filter-label">Topic</span>', `<span class="filter-label">${t.topic}</span>`);
  out = replaceText(out, '<span class="filter-label">Region</span>', `<span class="filter-label">${t.region}</span>`);
  out = replaceText(out, '<span class="filter-label">Country</span>', `<span class="filter-label">${t.country}</span>`);
  out = replaceText(out, '<span class="filter-label">Timezone</span>', `<span class="filter-label">${t.timezone}</span>`);
  out = replaceText(out, '<span class="filter-label">Level</span>', `<span class="filter-label">${t.level}</span>`);
  out = out.replace(/(<button class="filter-btn active" data-cat="all">)[^<]*(<\/button>)/, `$1${t.all}$2`);
  out = out.replace(/(<button class="filter-btn" data-cat="motor">)[^<]*(<\/button>)/, `$1${t.motor}$2`);
  out = out.replace(/(<button class="filter-btn" data-cat="sport">)[^<]*(<\/button>)/, `$1${t.sport}$2`);
  out = out.replace(/(<button class="filter-btn" data-cat="festival">)[^<]*(<\/button>)/, `$1${t.festival}$2`);
  out = out.replace(/(<button class="filter-btn" data-cat="culture">)[^<]*(<\/button>)/, `$1${t.culture}$2`);
  out = out.replace(/(<button class="filter-btn" data-cat="nature">)[^<]*(<\/button>)/, `$1${t.nature}$2`);
  out = out.replace(/(<button class="filter-btn active" data-cont="all">)[^<]*(<\/button>)/, `$1${t.all}$2`);
  out = out.replace(/(<button class="filter-btn active" id="location-toggle">)[^<]*(<\/button>)/, `$1${t.myTimezone}$2`);
  out = out.replace(/(<button class="filter-btn active" data-reach="all">)[^<]*(<\/button>)/, `$1${t.all}$2`);
  out = out.replace(/(<button class="filter-btn" data-reach="global"[^>]*>)[^<]*(<\/button>)/, `$1${t.global}$2`);
  out = out.replace(/(<button class="filter-btn" data-reach="continent"[^>]*>)[^<]*(<\/button>)/, `$1${t.continent}$2`);
  out = out.replace(/(<button class="filter-btn" data-reach="timezone"[^>]*>)[^<]*(<\/button>)/, `$1${t.timezoneLevel}$2`);
  out = out.replace(/All topics/g, t.allTopics);
  out = out.replace(/All countries/g, t.allCountries);
  out = out.replace(/All rights reserved\./g, t.footer);
  return out;
}

for (const [lang, t] of Object.entries(translations)) {
  const file = `${lang}/content/events/index.html`;
  if (!fs.existsSync(file)) continue;
  fs.writeFileSync(file, translate(fs.readFileSync(file, 'utf8'), t), 'utf8');
}

